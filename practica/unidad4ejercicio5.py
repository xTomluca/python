print("\nIngrese primer numero:")
numero_uno = int(input())
print("\nIngrese segundo numero:")
numero_dos = int(input())
print("\nIngrese tercer numero:")
numero_tres = int(input())
print("\nIngrese cuarto numero:")
numero_cuatro = int(input())
print("\nIngrese quinto numero:")
numero_cinco = int(input())

if numero_uno % 2 == 0:
    print("\nEl primer numero ingresado: {0} es divisible por 2\n".format(numero_uno))
else:
    print("El primer numero ingresado: {0} es no es divisible por 2\n".format(numero_uno))
if numero_dos % 2 == 0:
    print("El segundo numero ingresado: {0} es divisible por 2\n".format(numero_dos))
else:
    print("El segundo numero ingresado: {0} es no es divisible por 2\n".format(numero_dos))
if numero_tres % 2 == 0:
    print("El terce numero ingresado: {0} es divisible por 2\n".format(numero_tres))
else:
    print("El tercer numero ingresado: {0} es no es divisible por 2\n".format(numero_tres))
if numero_cuatro % 2 == 0:
    print("El cuarto numero ingresado: {0} es divisible por 2\n".format(numero_cuatro))
else:
    print("El cuarto numero ingresado: {0} es no es divisible por 2\n".format(numero_cuatro))
if numero_cinco % 2 == 0:
    print("El quinto numero ingresado: {0} es divisible por 2\n".format(numero_cinco))
else:
    print("El quinto numero ingresado: {0} es no es divisible por 2\n".format(numero_cinco))